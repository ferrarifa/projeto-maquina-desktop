    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed

        int selectedRow = tablePecas.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma PEÇA para exluir", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int pecaId = (int) tablePecas.getValueAt(selectedRow, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "Exluir PEÇA?", "Confirmação", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            
            PecasDAO dao = new PecasDAO();
            dao.deletar(pecaId);
            JOptionPane.showMessageDialog(this, "Peça deletada", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            preencherTabela();
        }
        
        Inicio inicio = new Inicio ();
        inicio.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnMaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMaisActionPerformed
        // TODO add your handling code here:
        
            int linha = tablePecas.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma peça!");
        return;
    }

        try {
            int id = Integer.parseInt(tablePecas.getValueAt(linha, 0).toString());
            int quantidade = Integer.parseInt(tablePecas.getValueAt(linha, 7).toString());

           int novaQuantidade = quantidade + 1;

        PecasDAO dao = new PecasDAO();
        dao.atualizarQuantidade(id, novaQuantidade);

       // Atualiza na tabela
        preencherTabela();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao atualizar quantidade!");
        e.printStackTrace();
    }

    }//GEN-LAST:event_btnMaisActionPerformed

    private void btnMenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMenosActionPerformed
        // TODO add your handling code here:  
        
        int linha = tablePecas.getSelectedRow();

    if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma peça!");
        return;
    }

    try {
        int id = Integer.parseInt(tablePecas.getValueAt(linha, 0).toString());
        int quantidade = Integer.parseInt(tablePecas.getValueAt(linha, 7).toString());

        if (quantidade <= 0) {
                JOptionPane.showMessageDialog(null, "Estoque já está zerado!");
            return;
        }

        int novaQuantidade = quantidade - 1;

        PecasDAO dao = new PecasDAO();
        dao.atualizarQuantidade(id, novaQuantidade);

        // Atualiza na tabela
        preencherTabela();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao atualizar quantidade!");
        e.printStackTrace();
    }

    }//GEN-LAST:event_btnMenosActionPerformed

