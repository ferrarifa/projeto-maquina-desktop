    public void preencherTabela() {
        model = (DefaultTableModel) tablePecas.getModel();
        model.setRowCount(0);
        
       PecasDAO dao = new PecasDAO();
       List<PecasBean> pecas = dao.ler();

        for(PecasBean prod: pecas) {
            Object[] linha = {
                prod.getId_pecas(),
                prod.getNome(),
                prod.getDescricao(),
                prod.getCodigo_fabricante(),
                prod.getTipo(),
                prod.getMaterial(),
                prod.getCompatibilidade(),
                prod.getQuantidade_estoque(),
                prod.getPreco(),
                prod.getFornecedor()
            };
            
            model.addRow(linha);
        }
    }
        
    
        
        
